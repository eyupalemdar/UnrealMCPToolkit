GENERATED_TCP_WRAPPER_COUNT = len(GENERATED_TCP_WRAPPERS)


def build_tcp_call(tool_name: str, arguments: dict | None = None) -> dict:
    spec = GENERATED_TCP_WRAPPERS.get(tool_name)
    if spec is None:
        return {"success": False, "error": f"No generated TCP wrapper spec for {tool_name}"}
    arguments = arguments or {}
    param_names = {param["name"] for param in spec["params"]} | {param["name"] for param in spec.get("meta_params", [])}
    unexpected = sorted(set(arguments) - param_names)
    if unexpected:
        return {
            "success": False,
            "error": f"Unexpected generated TCP wrapper arguments for {tool_name}: {', '.join(unexpected)}",
        }
    params = {}
    all_params = list(spec["params"]) + list(spec.get("meta_params", []))
    missing = [param["name"] for param in all_params if param.get("required") and param["name"] not in arguments]
    if missing:
        return {
            "success": False,
            "error": f"Missing generated TCP wrapper arguments for {tool_name}: {', '.join(missing)}",
        }
    for param in spec["params"]:
        name = param["name"]
        if name not in arguments:
            continue
        value = arguments[name]
        include = param.get("include", "always")
        if include == "when_truthy" and not value:
            continue
        if include == "when_not_none" and value is None:
            continue
        if include == "when_gt_zero" and not (isinstance(value, (int, float)) and value > 0):
            continue
        if include == "when_ge_zero" and not (isinstance(value, (int, float)) and value >= 0):
            continue
        if include == "when_provided" and value == param.get("default"):
            continue
        params[name] = value
    meta = {}
    for param in spec.get("meta_params", []):
        name = param["name"]
        if name not in arguments:
            continue
        value = arguments[name]
        include = param.get("include", "always")
        if include == "when_truthy" and not value:
            continue
        if include == "when_provided" and value == param.get("default"):
            continue
        meta[param.get("meta_key", name)] = value
    return {
        "success": True,
        "command": spec["command"],
        "params": params if spec["params"] else None,
        "meta": meta or None,
    }
